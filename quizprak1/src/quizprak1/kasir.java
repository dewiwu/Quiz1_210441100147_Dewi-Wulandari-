    private void selesaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selesaiActionPerformed
        // TODO add your handling code here:
        menu = (String) pilihkopi.getSelectedItem();

        switch (menu) {
            case "Arabica Coffee" -> hargaitem = 20000;
            case "Robusta Coffee" -> hargaitem = 25000;
            case "Liberian Coffee" -> hargaitem = 30000;
            case "Exelsa Coffee" -> hargaitem = 35000;
            default -> JOptionPane.showMessageDialog(this, "Pilihan tidak valid.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_selesaiActionPerformed

    private void hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungActionPerformed
        // TODO add your handling code here:
        int hargaitem = Integer.parseInt(panggilharga.getText()); 
        int jumlahPesan = Integer.parseInt(jmlpesanan.getText());
        
        int totalHarga = hargaitem * jumlahPesan;
        
        hitungtotal.setText(String.valueOf(totalHarga));
    }//GEN-LAST:event_hitungActionPerformed

