    private void panggilitemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_panggilitemActionPerformed
        // TODO add your handling code here:
        menu = (String) pilihkopi.getSelectedItem();
        int harga = 0;
        menu = (String)pilihkopi.getSelectedItem();
                switch (menu){
                    case "Arabica Coffe" -> {
                        menu = "Arabica Coffe";
                        hargaitem = (int) 20000;
            }
                    case "Robusta Coffe" -> {
                        menu = "Robusta Coffe";
                        hargaitem = (int) 25000;
            }
                    case "Liberian Coffe" -> {
                        menu = "Liberian Coffe";
                        hargaitem = (int) 30000;
            }
                    case "Exelsa Coffe" -> {
                        menu = "Exelsa Coffe";
                        hargaitem = (int) 35000;
            }   
        }
                hargaitem = harga;
    }//GEN-LAST:event_panggilitemActionPerformed

